<script>

        var toLock = [];
        var unLock = [];

          $(document).on('click', '.order_number', function(){


              if(!$(this).hasClass('locked')){
                $(this).toggleClass('to_lock');
              }

              if($(this).hasClass('locked')){
                 $(this).removeClass('locked')
                  unLock.push($(this).attr('data-id'));
              }

          });

          $(document).on('click', '#save-order', function(){
              $('.to_lock').each(function() {
                  toLock.push($(this).attr('data-id'));
              }).get();

              var data = {'toLock' : toLock, 'unLock' : unLock};

               $.ajax({
                  type  : 'POST',
                  url   : 'orders/store',
                  data  : {
                      '_token'  : $('meta[name="csrf-token"]').attr('content'),
                      'orders'      : data,
                  },
                  success: function(data){
                      window.location.reload();
                  }
              });
          });


    </script>
